import matplotlib.pyplot as plt

data = [
    (10, 5.209e-06),
    (1231, 7.7792e-05),
    (2649, 9.0041e-05),
    (5632, 0.000215792),
    (318169, 0.00693571),
    (1532618, 0.0185917),
    (2123121, 0.0214435),
    (10757981, 0.0853652),
    (71890209, 0.677531),
    (711202091, 7.32391)
]

# Разделение данных на два списка: один для оси X, другой для оси Y
x_values, y_values = zip(*data)

# Создание графика
plt.figure(figsize=(10, 6))
plt.semilogx(x_values, y_values, marker='o', linestyle='-')

# Настройка осей и меток
plt.xlabel('Входные данные')
plt.ylabel('Время выполнения (секунды)')
plt.title('График времени выполнения в зависимости от входных данных')

# Отображение графика
plt.grid(True)
plt.show()